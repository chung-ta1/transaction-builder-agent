cat > .claude/skills/delete-draft/SKILL.md <<'HEADER'
---
name: delete-draft
description: Permanently delete an unsubmitted draft from arrakis. Triggers when the user says "delete draft X", "cancel the draft", "throw away the draft", "abandon the draft", "scrap this one". Irreversible — runbook previews what's being deleted before firing DELETE /transaction-builder/{id}. For SUBMITTED transactions use termination, not this.
---

<!-- AUTO-GENERATED from src/prompts/delete_draft.md — do NOT edit this file directly. Edit the source and re-run `npm run build`. -->

HEADER
cat "$SRC_DELETE" >> .claude/skills/delete-draft/SKILL.md

