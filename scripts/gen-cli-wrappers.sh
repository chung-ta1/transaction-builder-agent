cat > .claude/skills/list-drafts/SKILL.md <<'HEADER'
---
name: list-drafts
description: Show the user's in-flight (unsubmitted) drafts. Triggers when the user says "what drafts do I have", "list my drafts", "show pending drafts", "any open drafts", "show me my last N drafts". Read-only — lists builderId, type, property, amount, status, and offers row-number shortcuts for follow-up actions.
---

<!-- AUTO-GENERATED from src/prompts/list_drafts.md — do NOT edit this file directly. Edit the source and re-run `npm run build`. -->

HEADER
cat "$SRC_LIST" >> .claude/skills/list-drafts/SKILL.md

# Delete any previously-generated subagent file if still present (legacy).
if [[ -f .claude/agents/transaction-creator.md ]]; then
  rm -f .claude/agents/transaction-creator.md
  echo "  ✗ Removed legacy .claude/agents/transaction-creator.md (subagents don't work with our MCP pattern)."
fi
# Remove empty .claude/agents if we created it previously and it's now empty.
if [[ -d .claude/agents ]] && [[ -z "$(ls -A .claude/agents 2>/dev/null)" ]]; then
  rmdir .claude/agents
fi

echo "✓ CLI wrappers generated from src/prompts/*.md"
echo "  .claude/skills/create-transaction/SKILL.md"
echo "  .claude/skills/sync-rules/SKILL.md"
echo "  .claude/skills/resume-draft/SKILL.md"
echo "  .claude/skills/create-listing/SKILL.md"
echo "  .claude/skills/create-referral/SKILL.md"
echo "  .claude/skills/create-referral-payment/SKILL.md"
echo "  .claude/skills/update-draft/SKILL.md"
echo "  .claude/skills/submit-draft/SKILL.md"
echo "  .claude/skills/delete-draft/SKILL.md"
echo "  .claude/skills/list-drafts/SKILL.md"
