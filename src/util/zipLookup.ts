/**
 * Best-effort guess of state/province + country + currency from a postal
 * code. Returns undefined when the format is unrecognized or the prefix
 * isn't in the lookup tables.
 */
export function guessFromPostalCode(postal: string): LocationGuess | undefined {
  const trimmed = postal.trim().toUpperCase();
  if (!trimmed) return undefined;

  const usMatch = /^(\d{5})(?:-\d{4})?$/.exec(trimmed);
  if (usMatch) {
    const prefix = usMatch[1].slice(0, 3);
    const state = US_ZIP3[prefix];
    if (!state) return undefined;
    return { state, country: "UNITED_STATES", currency: "USD" };
  }

  // Canadian format: A1A 1A1 (with or without space).
  const caMatch = /^([ABCEGHJKLMNPRSTVXY])\d[A-Z] ?\d[A-Z]\d$/.exec(trimmed);
  if (caMatch) {
    const state = CANADIAN_LETTER_TO_PROVINCE[caMatch[1]];
    if (!state) return undefined;
    return { state, country: "CANADA", currency: "CAD" };
  }

  return undefined;
}

export function currencyFor(state: StateOrProvince): "USD" | "CAD" {
  return CANADIAN_PROVINCES.has(state) ? "CAD" : "USD";
}
